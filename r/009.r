#!/usr/bin/Rscript

## c = 1000 - a - b
## a^2 + b^2 = (1000 - a - b)^2
## a^2 + b^2 = (1000 - a - b)(1000 - a - b)
## a^2 + b^2 = 1e6 - 1000a - 1000b - 1000a + a^2 + ab - 1000b +ab +b^2
## a^2 + b^2 = 1e6 - 2000a - 2000b + a^2 + 2ab +b^2
## 0 = 1e6 - 2000a - 2000b + 2ab
## 2000a + 2000b = 1e6 + 2ab
## a + b = 500 + ab/1000

for (a in 1:1000) {
    for (b in a:1000) {
        if (a + b == a * b / 1000 + 500) {
            c = 1000 - a - b
            cat(a * b * c, "\n")
        }
    }
}
